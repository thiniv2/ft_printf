#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*str;

	str = (char *)malloc(sizeof(char) * size + 1);
	while (size + 1 > 0)
	{
		str[size] = '\0';
		size--;
	}
	return (str);
}
